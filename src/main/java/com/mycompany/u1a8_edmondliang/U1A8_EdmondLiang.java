    private void btnsumalltheoddnumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsumalltheoddnumActionPerformed
        int oddsum = 0;
        // Iterate through the array to calculate the sum of odd numbers
        for (int i=0; i<Integerarry.length; i++){
            if (Integerarry[i]!=null&&Integer.parseInt(Integerarry[i]) % 2 != 0){
                // Adds the value of the current index of the array to the sum if it is odd
                oddsum += Integer.parseInt(Integerarry[i]);
            }
        }
        lblsum.setText("The sum of all odd number is "+oddsum);
        // Displays the sum
    }//GEN-LAST:event_btnsumalltheoddnumActionPerformed

