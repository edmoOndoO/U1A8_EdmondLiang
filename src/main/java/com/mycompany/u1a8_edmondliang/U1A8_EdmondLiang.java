    private void btnaddingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddingActionPerformed
        try{
            // Try to add the entered integer to the array
            if (Integer.parseInt(txtinput.getText())>=0){
            // Make sure that the user's input is a positive integer
                for (int i=0; i<Integerarry.length; i++){
                    // A for loop to iterate through the array
                    if (Integerarry[i] == null){
                    // If the array at this index is equal to null (empty), replace it with the user's integer
                        Integerarry[i] = txtinput.getText();
                        break;
                    }
                    if (i==19){
                    // If there is no empty slot, display a message
                        lblsum.setText("You have reached the maximum amount of number");
                    }
                }
            }
            else{
                // If the entered integer is less than 0, display an error message
                lblsum.setText("Invalid entry, please enter an integer greater than 0");
            }
        }
        catch (NumberFormatException e){
            // If the user enters anything other than a number, display an error message
            lblsum.setText("Invalid entry, please enter an integer greater than 0");
        }
    }//GEN-LAST:event_btnaddingActionPerformed

    private void btnlistActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnlistActionPerformed
        tareaarray.setText("");
        // Resets the array display
        for (int i=0; i<Integerarry.length; i++){
            // Iterates through the length of the array
            if (Integerarry[i]==null){
                continue;
                // Skip empty slots in the array
            }
            tareaarray.setText(tareaarray.getText()+Integerarry[i]+"\n");
            // Append the current index of the array to the text area
        }
        for (int i=0; i<Integerarry.length; i++){
            // Iterate through the length of the array
            if (Integerarry[i]==null){
                continue;
                // Skip empty slots in the array
            }
            if (i==0){
            // If it is the first integer, no comma will precede it
                lblsum.setText(Integerarry[i]);
            }
            else{
                lblsum.setText(lblsum.getText()+", "+Integerarry[i]);
            }
            // Display a comma-separated list of array number
        }
    }//GEN-LAST:event_btnlistActionPerformed

    private void btnremoveintegerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnremoveintegerActionPerformed
        try{
            // Try to remove the specified integer from the array
            for (int i=Integerarry.length-1; i>-1; i--){
                // Iterate through the array in reverse order
                if (Integerarry[i]==null){
                    // Skip the iteration if the index is empty
                    continue;
                }
                if (Integer.parseInt(Integerarry[i]) == Integer.parseInt(txtinput.getText())){
                // If the array at the index is equal to the user's integer, set it to null
                    Integerarry[i] = null;
                    for (int l=i; l<Integerarry.length-1;l++){
                        if (Integerarry[l+1]!=null){
                            Integerarry[l] = Integerarry[l+1];
                            Integerarry[l+1] = null;
                        }
                        // Move the newly made empty index to the end with the other empty slots
                    }
                    break;
                }
                if (i==0){
                // If the for loop reaches the last iteration without finding the user's integer, display a message
                    lblsum.setText("Integer not found within list");
                }
            }
        }
        catch (NumberFormatException e){
            // If the user enters anything other than a number, display an error message
            lblsum.setText("Invalid entry, please enter an integer greater than 0");
        }
    }//GEN-LAST:event_btnremoveintegerActionPerformed

